#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (NULL == new)
	{
		return (NULL);
	}
	new->content = content;
	new->next = NULL;
	return (new);
}
